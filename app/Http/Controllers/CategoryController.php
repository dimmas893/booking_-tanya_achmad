     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {

        $request->validate([
            'name' => 'required',
        ]);

        Category::create([
            'name' => $request->name,
        ]);
        toast('Data Category berhasil ditambahkan')->autoClose(2000)->hideCloseButton();
        return redirect()->back();
    }

